package com.example.movie_moa.findTheather;

public class AreaTheatherItem {
    public String cd;
    public String cdNm;


    public AreaTheatherItem(String cd, String cdNm) {
        this.cd = cd;
        this.cdNm = cdNm;
    }

    public String getCd() {
        return cd;
    }

    public void setCd(String cd) {
        this.cd = cd;
    }

    public String getCdNm() {
        return cdNm;
    }

    public void setCdNm(String cdNm) {
        this.cdNm = cdNm;
    }
}
